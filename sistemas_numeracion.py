# profundizando sistemas numeracion
# decimal
a = 10
# Binario tiene un 0b
a = 0b1010
# Octal tiene un 0o
a = 0o12
# hexadecimal tiene un 0x
a = 0xA
print(f"a:{a}")

# Convertir un tipo entero incluyendo la base
# base decimal
b = int("63",10)
# base binaria
b = int("10101001", 2)
# base octal
b = int("44", 8)
# base hexa
b = int("18", 16)
# Base 5
b = int("14",5)
print(f"b:{b}")